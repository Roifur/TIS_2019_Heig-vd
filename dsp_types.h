#ifndef _DSPTYPES_H_
#define _DSPTYPES_H_
/** \typedef uint8
 * \brief unsigned int, 8bit */
typedef unsigned char uint8;
/** \typedef int8
 * \brief signed int, 8bit */
typedef char int8;
/** \typedef uint16
 * \brief unsigned int, 16bit */
typedef unsigned short uint16;
/** \typedef int16
 * \brief signed int, 16bit */
typedef short int16;
/** \typedef uint32
 * \brief unsigned int, 32bit */
typedef unsigned int uint32;
/** \typedef int32
 * \brief signed int, 32bit */
typedef int int32;
/** \typedef uint40
 * \brief unsigned int, 40bit */
typedef unsigned long uint40;
/** \typedef int40
 * \brief signed int, 40bit */
typedef long int40;

#endif
